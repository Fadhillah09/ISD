class Pasien {
    String nomor;
    String namaPasien;
    String namaDokter;
    String jenisDokter;

    public Pasien(String nomor, String namaPasien, String namaDokter, String jenisDokter) {
        this.nomor = nomor;
        this.namaPasien = namaPasien;
        this.namaDokter = namaDokter;
        this.jenisDokter = jenisDokter;
    }
}